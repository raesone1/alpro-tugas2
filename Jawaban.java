public class Jawaban {
    public static void main(String[] args) {
        // Tipe Data Primitif

        // Tipe Data Integer
        int angkaBulanan = 30;
        System.out.println("Tipe Data Integer: " + angkaBulanan);

        // Tipe Data Long
        long angkaBesar = 100000L;
        System.out.println("Tipe Data Long: " + angkaBesar);

        // Tipe Data Byte
        byte angkaKecil = 127;
        System.out.println("Tipe Data Byte: " + angkaKecil);

        // Tipe Data Short
        short angkaPendek = 30000;
        System.out.println("Tipe Data Short: " + angkaPendek);

        // Tipe Data Floating Point
        float angkaDesimal = 3.14f;
        System.out.println("Tipe Data Float: " + angkaDesimal);

        // Tipe Data Double
        double angkaDouble = 3.14159;
        System.out.println("Tipe Data Double: " + angkaDouble);

        // Tipe Data Karakter
        char huruf = 'A';
        System.out.println("Tipe Data Char: " + huruf);

        // Tipe Data Boolean
        boolean isJavaMenarik = true;
        System.out.println("Tipe Data Boolean: " + isJavaMenarik);

        // Tipe Data Referensi (Contoh)
        String teks = "Halo, Semuanya!";
        System.out.println("Tipe Data String: " + teks);
    }
}

//Penjelasan Tipe Data:
//1.Integer: Tipe data untuk bilangan bulat.
//2.Long: Tipe data untuk bilangan bulat dengan kapasitas lebih besar.
//3.Byte: Tipe data untuk bilangan bulat kecil (range -128 to 127).
//4.Short: Tipe data untuk bilangan bulat kecil (range -32,768 to 32,767).
//5.Float: Tipe data untuk angka desimal dengan presisi tunggal.
//6.Double: Tipe data untuk angka desimal dengan presisi ganda.
//7.Char: Tipe data untuk karakter tunggal.
//8.Boolean: Tipe data untuk nilai true/false.
//9.String: Tipe data referensi untuk urutan karakter (teks).

