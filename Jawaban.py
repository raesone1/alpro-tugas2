def main():
    # Tipe Data Primitif

    # Tipe Data Integer
    angka_bulanan = 30
    print("Tipe Data Integer:", angka_bulanan)

    # Tipe Data Float
    angka_desimal = 3.14
    print("Tipe Data Float:", angka_desimal)

    # Tipe Data Complex
    angka_kompleks = 2 + 3j
    print("Tipe Data Complex:", angka_kompleks)

    # Tipe Data String
    teks = "Halo, Semuanya!"
    print("Tipe Data String:", teks)

    # Tipe Data Boolean
    is_python_menarik = True
    print("Tipe Data Boolean:", is_python_menarik)

    # Tipe Data List
    daftar_angka = [1, 2, 3, 4, 5]
    print("Tipe Data List:", daftar_angka)

    # Tipe Data Tuple
    tupel_angka = (1, 2, 3)
    print("Tipe Data Tuple:", tupel_angka)

    # Tipe Data Dictionary
    kamus = {'nama': 'Alice', 'usia': 25}
    print("Tipe Data Dictionary:", kamus)

    # Tipe Data Set
    himpunan = {1, 2, 3, 4, 5}
    print("Tipe Data Set:", himpunan)

if __name__ == "__main__":
    main()

#Penjelasan Tipe Data:
#1.Integer: Tipe data untuk bilangan bulat.
#2.Float: Tipe data untuk angka desimal.
#3.Complex: Tipe data untuk bilangan kompleks.
#4.String: Tipe data untuk urutan karakter (teks).
#5.Boolean: Tipe data untuk nilai True atau False.
#6.List: Tipe data untuk koleksi terurut yang dapat diubah.
#7.Tuple: Tipe data untuk koleksi terurut yang tidak dapat diubah.
#8.Dictionary: Tipe data untuk koleksi pasangan kunci-nilai.
#9.Set: Tipe data untuk koleksi unik yang tidak terurut.